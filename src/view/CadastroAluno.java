    private void jbSalvarAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarAlunoActionPerformed
        if(jtfNomeAluno.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Preencha o nome do Aluno","Aluno - Aviso", JOptionPane.WARNING_MESSAGE);
        }else{
            aluno.setNome(jtfNomeAluno.getText());
            alunoDao.inserir(aluno);
        }
        
        atualizarTabelaAluno();
        limparCampos();
        
    }//GEN-LAST:event_jbSalvarAlunoActionPerformed

    private void jtAlunoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtAlunoMouseClicked
        jtfCodAluno.setText(jtAluno.getValueAt(jtAluno.getSelectedRow(), AlunoTableModel.COL_CODIGO).toString());
        jtfNomeAluno.setText(jtAluno.getValueAt(jtAluno.getSelectedRow(), AlunoTableModel.COL_NOME).toString());
    }//GEN-LAST:event_jtAlunoMouseClicked

    private void jbExcluirAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirAlunoActionPerformed
        int linha = jtAluno.getSelectedRow();
        Object registroSelecionado = jtAluno.getValueAt(linha, 0);
        alunoDao.excluir((int)registroSelecionado); 
    }//GEN-LAST:event_jbExcluirAlunoActionPerformed

